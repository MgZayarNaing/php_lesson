<!-- PHP switch Statement -->
<?php
    $color = "skyblue";

    switch ($color) {
        case "red":
          echo "My favorite color is red!";
          break;
        case "blue":
          echo "My favorite color is blue!";
          break;
        case "black":
          echo "My favorite color is black!";
          break;
        default:
          echo "I don't like color !";
      }
?>